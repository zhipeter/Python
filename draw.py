from turtle import Turtle
def a():
	turtle.speed(2)
	turtle.pensize(10)
	turtle.color("#ff0000",'yellow')
	turtle.begin_fill()
	for i in range(5):
		turtle.forward(150)
		turtle.right(144)
	turtle.end_fill()
def main():
    turtle.setup(1300,800,0,0)
    pythonsize=30
    turtle.pensize(pythonsize)
    turtle.pencolor("#FF7D40")
    turtle.seth(0)
    turtle.fd(80)
    turtle.seth(-60)
    turtle.circle(60,300)
    turtle.seth(90)
    turtle.fd(200)
    turtle.circle(40,180)
    turtle.fd(200)
    turtle.seth(120)
    turtle.circle(60,300)
a()
turtle.penup()
turtle.goto(-350,-200)
turtle.pendown()
main()
turtle.penup()
turtle.goto(350,-200)
turtle.pendown()
main()